C’est quoi la PNL ?
La “Programmation Neuro Linguistique” a été mise au point dans les années 1970 aux Etats-Unis par Richard Bandler et John Grinder.

C’est une thérapie brève qui permet à la personne de changer la façon dont elle perçoit son environnement, d’élargir ses possibilités de comportements et d’améliorer ses capacités pour atteindre ses objectifs.

Cette méthode s’intéresse plus au “comment ça marche quand ça marche” plutôt qu’au “pourquoi ça ne marche pas” et nous amène à voir le verre à moitié plein plutôt qu’à moitié vide.

La programmation
Tout au long de notre vie, nous programmons des façons d’être, de ressentir, de nous comporter en fonction de ce que nous vivons et rencontrons. 
Tout programme peut être décodé pour être modifié, s'il n'est plus adapté au bien-être de la personne.

Neuro
L’humain perçoit, pense, ressent et opère des choix grâce à son cerveau, son système nerveux et ses cinq sens.
Toute notre expérience est acquise dans les processus neurologiques.

Linguistique
Notre langage structure et reflète la manière dont nous pensons. Nous retranscrivons notre expérience par nos mots, notre façon de parler et notre langage corporel.


A quoi ça sert ?
La PNL permet d’ajuster notre façon de voir le monde ainsi que d’améliorer notre manière de communiquer et d’agir au quotidien en modifiant nos comportements et leurs résultats.

Cette technique permet de prendre conscience de son propre potentiel et cela passe notamment par l’observation, la déconstruction de croyances et de comportements ancrés depuis l’enfance ou des évènements de vie marquants. 

Des outils tels que la modélisation ou la technique d’ancrage sont utilisés et permettent de reproduire ou déconstruire certaines façons d’agir qui ne sont plus adaptées au bien-être de la personne.

Exemples: 
- prendre confiance en soi
- dépasser des sentiments négatifs
- gérer son stress
- gérer sa timidité
- dépasser ses phobies
- améliorer ses relations au travail
- atteindre ses objectifs
- mieux vivre un deuil, une séparation ou une maladie etc…

Quelques présupposés de la PNL:
« La carte n’est pas le territoire »
Chacun voit le monde à travers ses propres filtres, qui ne correspondent pas à une réalité unique.

« Derrière chaque comportement, il y a une intention positive »
Le comportement d’une personne est le résultat d’un choix considéré comme le meilleur possible à un moment donné.

« Il n’y a pas d’échec, que des apprentissages »
Cette attitude permet de se donner la permission de commettre des erreurs et d’accepter celles de l’autre.

« On ne peut pas ne pas communiquer »
Nous sommes des êtres de communication. Tout est communication, même le refus de communication.

« Toute personne a en elle les ressources nécessaires pour accomplir son objectif »

« Le comportement d’une personne n’est pas cette personne »
Il faut faire la distinction entre l’identité d’une personne et ses comportements.


